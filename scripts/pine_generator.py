#!/usr/bin/env python3
"""
Generate Pine Script candidate files for validated spike winners.

The generator is explicit per strategy family. That is deliberate: the
optimizer can select materially different logic families, so emitting a
"best effort" approximation would be misleading.
"""

from __future__ import annotations

import os
from pathlib import Path
from textwrap import dedent

from deploy import ACTIVE_PINE_PATH, patch_pine_script


PROJECT_ROOT = Path(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))


def _pine_number(value, *, force_float: bool = False) -> str:
    if isinstance(value, bool):
        return "true" if value else "false"
    if isinstance(value, int) and not force_float:
        return str(value)
    text = f"{float(value):.6f}".rstrip("0").rstrip(".")
    if "." not in text:
        text += ".0"
    return text


def _header(title: str, shorttitle: str, strategy_name: str) -> str:
    return dedent(
        f"""\
        //@version=6
        strategy("{title}",
             overlay=true,
             shorttitle="{shorttitle}",
             initial_capital=1000,
             default_qty_type=strategy.percent_of_equity,
             default_qty_value=100,
             pyramiding=0,
             commission_type=strategy.commission.percent,
             commission_value=0.05,
             slippage=0,
             calc_on_every_tick=false,
             process_orders_on_close=true)

        // Generated by scripts/pine_generator.py
        // Source strategy family: {strategy_name}

        """
    )


def _wrap(title: str, shorttitle: str, strategy_name: str, body: str) -> str:
    footer = dedent(
        """

        riskState = strategy.position_size > 0 ? 1 : 0
        riskOn = riskState == 1
        riskOff = riskState == 0
        riskOnFlip = ta.change(riskState) > 0
        riskOffFlip = ta.change(riskState) < 0

        plot(riskState, "Risk State", color=color.new(color.gray, 100), display=display.data_window)
        alertcondition(riskOnFlip, "Risk On", "Project Montauk risk_state=risk_on")
        alertcondition(riskOffFlip, "Risk Off", "Project Montauk risk_state=risk_off")
        """
    ).strip()
    return _header(title, shorttitle, strategy_name) + dedent(body).strip() + "\n\n" + footer + "\n"


def _build_montauk_821(params: dict) -> str:
    return _wrap(
        "Project Montauk Candidate - Montauk 8.2.1 Logic",
        "Montauk Cand",
        "montauk_821",
        f"""
        shortEmaLen         = input.int({_pine_number(params.get("short_ema", 15))}, "Short EMA Length", minval=1, group="1 - Inputs")
        medEmaLen           = input.int({_pine_number(params.get("med_ema", 30))}, "Medium EMA Length", minval=1, group="1 - Inputs")
        trendEmaLen         = input.int({_pine_number(params.get("trend_ema", 70))}, "Trend EMA Length", minval=1, group="1 - Inputs")
        slopeLookback       = input.int({_pine_number(params.get("slope_lookback", 10))}, "Trend Slope Lookback", minval=1, group="1 - Inputs")
        atrPeriod           = input.int({_pine_number(params.get("atr_period", 40))}, "ATR Period", minval=1, group="1 - Inputs")
        atrMultiplier       = input.float({_pine_number(params.get("atr_mult", 3.0), force_float=True)}, "ATR Multiplier", step=0.1, group="1 - Inputs")
        quickEmaLen         = input.int({_pine_number(params.get("quick_ema", 15))}, "Quick EMA Length", minval=1, group="1 - Inputs")
        quickLookbackBars   = input.int({_pine_number(params.get("quick_lookback", 5))}, "Quick EMA Lookback", minval=1, group="1 - Inputs")
        quickDeltaPctThresh = input.float({_pine_number(params.get("quick_thresh", -8.2), force_float=True)}, "Quick EMA Delta Threshold", step=0.1, group="1 - Inputs")
        sellBufferPct       = input.float({_pine_number(params.get("sell_buffer", 0.2), force_float=True)}, "Sell Buffer Pct", step=0.01, group="1 - Inputs")
        cooldownBars        = input.int({_pine_number(params.get("cooldown", 0))}, "Cooldown Bars", minval=0, group="1 - Inputs")

        emaShort = ta.ema(close, shortEmaLen)
        emaMed   = ta.ema(close, medEmaLen)
        emaTrend = ta.ema(close, trendEmaLen)
        quickEma = ta.ema(close, quickEmaLen)
        atrVal   = ta.atr(atrPeriod)

        trendSlope = (emaTrend - emaTrend[slopeLookback]) / slopeLookback
        trendOk    = na(emaTrend) or na(emaTrend[slopeLookback]) or trendSlope > 0
        entrySignal = emaShort > emaMed and trendOk

        quickEmaPast  = quickEma[quickLookbackBars]
        quickDeltaPct = quickEmaPast != 0 ? 100.0 * (quickEma - quickEmaPast) / quickEmaPast : 0.0

        isAtrExit   = strategy.position_size > 0 and close < close[1] - atrVal * atrMultiplier
        isQuickExit = strategy.position_size > 0 and quickDeltaPct <= quickDeltaPctThresh
        isCrossExit = strategy.position_size > 0 and emaShort < emaMed * (1 - sellBufferPct / 100)

        exitCond = isAtrExit or isQuickExit or isCrossExit
        var string exitReason = na
        if strategy.position_size > 0
            if isAtrExit
                exitReason := "ATR Shock"
            else if isQuickExit
                exitReason := "Quick EMA"
            else if isCrossExit
                exitReason := "EMA Cross"

        var int lastSellBar = na
        if exitCond
            strategy.close("Long")
            lastSellBar := bar_index
            label.new(bar_index, high, exitReason, yloc=yloc.abovebar, style=label.style_label_down, color=color.red, textcolor=color.white, size=size.tiny)

        canEnter = strategy.position_size == 0 and (na(lastSellBar) or (bar_index - lastSellBar) > cooldownBars)
        if entrySignal and canEnter and not exitCond
            strategy.entry("Long", strategy.long)

        plot(emaShort, "EMA Short", color=color.new(color.teal, 0))
        plot(emaMed, "EMA Med", color=color.new(color.orange, 0))
        plot(emaTrend, "EMA Trend", color=color.new(color.blue, 30))
        """,
    )


