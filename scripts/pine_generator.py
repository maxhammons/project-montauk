def supports_pine_strategy(strategy_name: str) -> bool:
    return strategy_name in _BUILDERS


def supported_pine_strategies() -> list[str]:
    return sorted(_BUILDERS)


def generate_pine_script(strategy_name: str, params: dict) -> str:
    builder = _BUILDERS.get(strategy_name)
    if not builder:
        raise ValueError(f"No Pine generator for strategy {strategy_name}")
    return builder(params)


def write_candidate_strategy(run_dir: str | Path, strategy_name: str, params: dict) -> str:
    run_path = Path(run_dir)
    run_path.mkdir(parents=True, exist_ok=True)
    text = generate_pine_script(strategy_name, params)
    output_path = run_path / "candidate_strategy.txt"
    with open(output_path, "w", encoding="utf-8", newline="\n") as f:
        f.write(text)
    return str(output_path)


def write_montauk_patch(run_dir: str | Path, strategy_name: str, params: dict) -> str | None:
    if strategy_name != "montauk_821":
        return None

    with open(ACTIVE_PINE_PATH, "r", encoding="utf-8", newline="") as f:
        pine_text = f.read()

    patched_text, _changes, _unmatched = patch_pine_script(pine_text, params)
    output_path = Path(run_dir) / "patched_strategy.txt"
    with open(output_path, "w", encoding="utf-8", newline="\n") as f:
        f.write(patched_text)
    return str(output_path)
